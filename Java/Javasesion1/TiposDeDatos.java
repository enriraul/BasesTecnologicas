package Javasesion1;
public class TiposDeDatos{
    public static void main(String[] args) {
        //Enteros
        byte edad = 25;
        short año = 2024;
        int numero = 109293;
        long población = 60394950L;
        
        //Decimales
        float temperatura = 36.5f;
        double pi = 3.141592;
        
        //Caracteres 
        char letra = 'A';

        //Boolean
        boolean Bandera = true; //false

        boolean eduación = false;

        System.out.println("Impresiones de los tipos de datos de Java:");
        System.out.println("byte:" +edad);
        System.out.println("short: "+año);
        System.out.println("int:" +numero);
        System.out.println("boolean: "+eduación);

    }
}